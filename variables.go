package main

import "fmt"

func main() {
	var a = "initial"
	fmt.Println(a)

	var b, c int = 1, 2
	fmt.Println(b, c)

	var d = true
	fmt.Println(d)

	var e int
	fmt.Println(e)

	f := "apple"
	fmt.Println(f)

	var m = 10
	fmt.Println(m)

	var foo, bar = 34, 35
	fmt.Println(foo, bar)

	mango := 10
	fmt.Println("Value of mango:", mango)
}
